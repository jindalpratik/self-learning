public class naming_variables {
    public static void main(String[] args) {
        double a = 3.14;
        double b = 22.0;
        double c = a * b * b;

        System.out.println(c);

        double pi = 3.14;
        double radius = 22.0;
        double surfaceArea = pi * radius * radius;

        System.out.println(surfaceArea);
    }
}